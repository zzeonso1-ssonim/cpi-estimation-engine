def core_flags(code: str, name: str) -> tuple[int, int]:
    """공식 정의(붙임1) 기반 품목 단위 core 포함플래그."""
    # 근원① 식료품(농축수산·가공식품) + 에너지(석유류·전기료·도시가스·지역난방비) 제외
    #   → 전기가스수도 중 '상수도료'는 에너지가 아니므로 포함
